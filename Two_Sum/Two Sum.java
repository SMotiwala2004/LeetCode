class Solution {
    public int[] twoSum(int[] nums, int target) {
        for(int i = 0; i < nums.size(); i++){
            for(int j = 0; j < nums.size(); j++){
                int sum = nums[i] + nums[j];
                    if(sum = target) {
                        system.out.print(nums[i] +","+nums[j]);
                    }            }
        }
    }
}
